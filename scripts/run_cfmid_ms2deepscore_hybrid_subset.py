#!/usr/bin/env python3
"""Run a CFM-ID + MS2DeepScore hybrid CASMI subset benchmark.

This is not native MS2DeepScore. Candidate spectra are generated by CFM-ID for
the already explicit candidate-limited CASMI subset, then MS2DeepScore ranks the
query spectrum against those generated candidate spectra.
"""

from __future__ import annotations

import argparse
import concurrent.futures as futures
import json
import os
import signal
import subprocess
import time
from collections import defaultdict
from pathlib import Path
from typing import Any

import numpy as np
import pandas as pd
from matchms import Spectrum
from ms2deepscore import MS2DeepScore
from ms2deepscore.models import load_model


